                ¿Qué son los Reactores nucleares modulares?
            </h2>
                <h3>
                    Los reactores modulares pequeños (SMR) son reactores nucleares avanzados con una capacidad de potencia de hasta 300 MW(e) por unidad, lo que representa cerca de un tercio de la capacidad de generación de los reactores nucleares de potencia tradicionales. Los SMR, que pueden producir grandes cantidades de electricidad con bajas emisiones de carbono, son: <br>

                    <br> Pequeños: físicamente una fracción del tamaño de un reactor nuclear de potencia convencional.</br>
                    <br> Modulares: lo que permite que los sistemas y componentes se ensamblen en fábrica y se transporten como una sola unidad a un lugar para su instalación.</br>
                    <br> Reactores: que aprovechan la fisión nuclear para generar calor para producir energía.</br>
                </h3>
